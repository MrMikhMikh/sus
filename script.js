let dialogStep = 0
let currentDialog = []

const modelViewerWrap = document.getElementById('modelViewerWrap')

function startDialog(){
  centrikBox.style.backgroundImage = `url('${backgrounds[currentPoint-1]}')`
  currentDialog = dialogs[currentPoint-1]
  dialogStep = 0

  // Показываем 3D модель только на точке 8 (токарь)
  if(currentPoint === 8){
    modelViewerWrap.style.display = 'block'
  } else {
    modelViewerWrap.style.display = 'none'
  }

  showDialog(dialogStep)
}

function showDialog(step){
  speech.innerText = currentDialog[step].text
  answers.innerHTML = ""

  currentDialog[step].answers.forEach(answer => {
    let btn = document.createElement("button")
    btn.className = "answer"
    btn.innerText = answer.text
    btn.onclick = () => {
      clickSound.currentTime = 0
      clickSound.play()
      dialogStep = answer.next
      showDialog(dialogStep)
    }
    answers.appendChild(btn)
  })

  if(currentDialog[step].answers.length === 0){
    answers.innerHTML = ""

    if(currentPoint==1||currentPoint==3||currentPoint==6||currentPoint==7||currentPoint==8||currentPoint==9){
      addValue()
    }

    if(currentPoint === 10){
      setTimeout(() => {
        centrikBox.style.display = "none"
        finishSound.play()
        finalScreen.style.display = "flex"
        showFarewellMessage()
      }, 1500)
      return
    }

    nextQR.style.display = "block"
  } else {
    nextQR.style.display = "none"
  }
}

